def animate_verification():
    frames = ["[■□□□□□□□□□]", "[■■□□□□□□□□]", "[■■■□□□□□□□]", 
              "[■■■■□□□□□□]", "[■■■■■□□□□□]", "[■■■■■■□□□□]",
              "[■■■■■■■□□□]", "[■■■■■■■■□□]", "[■■■■■■■■■□]", 
              "[■■■■■■■■■■]"]
    
    for frame in frames:
        sys.stdout.write("\r\033[38;2;0;128;0mVerificando... " + frame)
        sys.stdout.flush()
        time.sleep(0.3)
    print("\n\033[38;2;0;255;0mSenha verificada com sucesso!\033[0m")
    time.sleep(1)

def password_check():
    attempts = 3
    while attempts > 0:
        show_banner()
        password = input("\n\033[38;2;255;255;255mDigite a senha: \033[0m")
        
        if password == "admin":
            animate_verification()
            time.sleep(1)
            os.system('clear')
            os.system('python main.py')
            return
        else:
            attempts -= 1
            print(f"\033[38;2;255;0;0mSenha incorreta! Tentativas restantes: {attempts}\033[0m")
            time.sleep(1)
    
    print("\033[38;2;255;0;0mAcesso negado! Encerrando...\033[0m")
    time.sleep(2)
    os.system('clear')
    sys.exit()

password_check()
